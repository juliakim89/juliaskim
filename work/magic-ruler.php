					<h3 class="subheader">Description</h3>
					<p>
						During our 3 day Tradesy hackathon, we set out to solve a real customer problem while utilizing the new Augmented Reality (AR) technology available on Apple. We wanted to use AR to help sellers take the hassle away from measuring their items.
					</p>
					<h3 class="subheader">Problems</h3>
					<ul>
						<li>Sellers have little to no guideline or tips on how to properly measure their bag for the listing process</li>
						<li>8 - 10% of listings have incorrect measurements such as 1 x 1 x 1</li>
						<li>Bags allowed to be listed at 0 x 0 x 0 cause confusion for buyers and dissatisfaction if bag is not what they expected</li>
						<li>Inaccurate bag measurements leads to returns found to be misrep affecting both buyer and seller experience</li>
						<li>Users mix up all 3 measurements (length, width, height)</li>
					</ul>
					<h3 class="subheader">Goals</h3>
					<ul>
						<li>Make it easier for users to get through the listing process by providing a fun way to measure items.</li>
						<li>Reduce the confusion users have when measuring their bags.</li>
					</ul>
					<h3 class="subheader">Success Metrics</h3>
					<ul>
						<li>Reduce the amount of misrep returns due to wrong bag measurements </li>
						<li>Reduce customer confusion when they get to bag measurements within listing process</li>
						<li>Reduce the amount of listings that have 0 x 0 x 0 for the measurements</li>
					</ul>
					<h3 class="subheader">Research Methods</h3>
					<ul>
