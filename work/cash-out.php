				<h3 class="subheader">Validated Hypotheses</h3>
				<ul>
					<li>Users don't know when their loan is ready for pick up</li>
					<li>Users can't find the info they need to cash out</li>
					<li>Users aren't sure what is the right cash out channel for them</li>
					<li>Users who experience problems with cash out are more likely to churn</li>
					<li>Users feel scared when they don't know the status of their loan between disbursement and pickup</li>
				</ul>
			</div>
			
			
		</div>
				

