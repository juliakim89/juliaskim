					<li>Customer shadowing</li>
					<li>Diary study</li>
					<li>User interviews</li>
				</ul>
				<h3 class="subheader">Success Metrics</h3>
				<ul>
					<li>Reduce cashout tickets</li>
					<li>Reduce # of steps</li>
					<li>Increase conversion</li>
					<li>Improve customer satisfaction score</li>
				</ul>
			</div>
