				<h3 class="subheader">Pain points</h3>
				<ul>
					<li>Designs were too text heavy for our users who skim</li>
					<li>Low comprehension on "padala" vs "padala express"</li>
					<li>Majority of users needed to tap between multiple screens and dropdowns to find preferred channel</li>
					<li>Users had to scroll through a dropdown of 40+ bank cashout options, when the top 6 banks make up 85% of bank cashouts</li>
					<li>Fees were not specified for loan amount until 4th step</li>
					<li>Low contrast and small font not great for small devices, broken, or scratched screens</li>
					<li>Requirements to pick up loan hidden in heavy small text</li>
					<li>Nearly 50% of tickets were about the status of their loan</li>
					<li>Estimated time frame on when to expect their loan wasn't clear</li>
				</ul>
			</div>
		</div>

		<h3 class="subheader">Cashout User Journey</h3>
